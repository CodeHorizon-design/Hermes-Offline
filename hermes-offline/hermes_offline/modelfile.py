"""
Modelfile auto-generator for hermes-offline.

Generates an optimally-tuned Ollama Modelfile for the current hardware tier,
then registers it with Ollama as a named model variant (e.g. `qwen3:8b-hermes`).

Why custom Modelfiles?
──────────────────────
The default Ollama model parameters are designed for interactive chat, not
agentic tool-calling. Hermes benefits from:

  num_ctx        — must match hermes's context window config exactly
  num_thread     — set to physical core count, not logical (avoids hyperthreading waste)
  temperature    — lower (0.15) for reliable tool calling; default is 0.8
  mirostat 2     — adaptive sampling keeps responses on-track on low-end hardware
  repeat_penalty — slightly higher (1.1) reduces loops and hallucinations
  num_keep       — protect system prompt tokens from being evicted
  stop tokens    — hermes-specific stop sequences

Hardware tier parameters:
  ultra_low  4 GB RAM  num_ctx  4096   temp 0.15  threads = cpu_cores
  low        8 GB RAM  num_ctx  8192   temp 0.15  threads = cpu_cores
  mid        12 GB RAM num_ctx 16384   temp 0.20  threads = cpu_cores
  good       16 GB RAM num_ctx 32768   temp 0.20  threads = cpu_cores
  great      24+ GB    num_ctx 65536   temp 0.25  threads = cpu_cores

Usage:
  from hermes_offline.modelfile import generate_and_register

  # Auto-detect hardware, generate, register with Ollama
  path, model_name = generate_and_register()

  # Or generate for a specific model + tier
  path, model_name = generate_and_register(model="qwen3:8b", tier="low")
"""

