"""
Offline-first interactive setup wizard for Hermes Agent.

This wizard replaces the default hermes setup for offline use:
  1. Detect hardware (RAM, VRAM, CPU)
  2. Check if Ollama is running; offer to install it
  3. List installed models; recommend the best for detected hardware
  4. Offer to pull the recommended model
  5. Write ~/.hermes/config.yaml with offline-first defaults
  6. Optional: setup gateway (Telegram, Discord, etc.)
  7. Optional: install piper-tts for local voice
  8. Optional: install faster-whisper for local transcription

Run standalone:
    python -m hermes_offline.setup
Or via entry point:
    hermes-offline-setup
"""

from __future__ import annotations

import json
import os
import platform
import subprocess
import sys
import urllib.request
from pathlib import Path
from typing import Optional

try:
    from rich.console import Console
    from rich.panel import Panel
    from rich.prompt import Confirm, Prompt
    from rich.table import Table
    console = Console()
except ImportError:
    # Minimal fallback without rich
    class Console:
        def print(self, *args, **kwargs): print(*args)
        def rule(self, *args, **kwargs): print("─" * 60)
    class Panel:
        def __new__(cls, content, **kwargs): return content
    class Confirm:
        @staticmethod
        def ask(q, default=True): 
            ans = input(f"{q} [{'Y/n' if default else 'y/N'}]: ").strip().lower()
            return (ans in ("y", "yes", "")) if default else (ans in ("y", "yes"))
    class Prompt:
        @staticmethod
        def ask(q, default=""): return input(f"{q} [{default}]: ").strip() or default
    console = Console()


def _default_hermes_home() -> Path:
    """Platform-aware default Hermes home directory."""
    env = os.environ.get("HERMES_HOME", "").strip()
    if env:
        return Path(env)
    if os.name == "nt":  # Windows
        local = os.environ.get("LOCALAPPDATA", "").strip()
        base = Path(local) if local else Path.home() / "AppData" / "Local"
        return base / "hermes"
    return Path.home() / ".hermes"

HERMES_HOME = _default_hermes_home()

